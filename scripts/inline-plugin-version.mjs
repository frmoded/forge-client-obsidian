// scripts/inline-plugin-version.mjs
//
// v0.2.131 — bake manifest.json's version string into main.js at
// build time. Per v0331 §2.1: the plugin's TypeScript source needs
// a constant that becomes part of compiled main.js. At onload, the
// plugin compares this baked-in constant against the manifest.json
// it reads off disk; mismatch → Notice + console.error so cohort
// users with stale BRAT installs see clear instructions instead of
// silently running old code.
//
// Mirrors the v0.2.98 inlined-asset version stamping discipline
// (B10 in the constitution): every release's compiled main.js
// carries its own self-claimed version.
//
// Output: src/version-constant.generated.ts (gitignore-friendly;
// regenerated on every build).
//
// Usage:
//   node scripts/inline-plugin-version.mjs

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");

const manifestPath = path.join(ROOT, "manifest.json");
const outPath = path.join(ROOT, "src", "version-constant.generated.ts");

const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
const version = manifest.version;

if (typeof version !== "string" || version.length === 0) {
  console.error(
    "[inline-plugin-version] manifest.json missing or empty 'version' field",
  );
  process.exit(1);
}

const contents = `// AUTO-GENERATED by scripts/inline-plugin-version.mjs at build time.
// Do not edit by hand. Source of truth is manifest.json's "version" field.
//
// v0.2.131 — baked into main.js so onload can self-check against the
// on-disk manifest.json. Stale-install detection per v0331.

export const PLUGIN_VERSION_AT_BUILD = ${JSON.stringify(version)};
`;

fs.writeFileSync(outPath, contents, "utf8");
console.log(`[inline-plugin-version] wrote ${path.relative(ROOT, outPath)} → ${version}`);
