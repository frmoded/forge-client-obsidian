// v0.2.91 — inline bundled plugin assets into the JS bundle so BRAT
// installs (which fetch only main.js + manifest + styles + data) get
// the vault content, engine bundle, iframe HTML, and welcome notes
// even though `assets/` never lands on disk.
//
// Walks the three asset trees we want inlined:
//   - assets/vaults/*    → forge-moda, forge-music, forge-tutorial snippets
//   - assets/engine/*    → forge engine .py files (Pyodide MEMFS targets)
//   - assets/iframe/*    → moda iframe HTML + SVGs
//   - assets/welcome/*   → welcome.md + greet.md (extracted to vault root)
//
// Skips:
//   - assets/pyodide/    → 16 MB; loaded via CDN at runtime per Phase 3
//   - assets/wheels/     → 23 MB; loaded via CDN at runtime per Phase 4
//
// Output: src/bundled-assets.generated.ts exporting a
// `BUNDLED_ASSETS: Record<string, string>` map. Keys are relative
// paths under `assets/` using forward slashes; values are file
// contents as UTF-8 strings (all files in scope are text — no
// binary today; if that changes, switch to base64 encoding).

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const ASSETS = path.join(ROOT, "assets");
const OUT = path.join(ROOT, "src", "bundled-assets.generated.ts");

const INLINE_DIRS = ["vaults", "engine", "iframe", "welcome"];

function walk(dir, base = "") {
  const out = [];
  if (!fs.existsSync(dir)) return out;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const rel = base ? `${base}/${entry.name}` : entry.name;
    const abs = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      out.push(...walk(abs, rel));
    } else if (entry.isFile()) {
      out.push({ rel, abs });
    }
  }
  return out;
}

function main() {
  console.log("=== inline-bundled-assets ===");
  console.log(`Source: ${ASSETS}`);
  console.log(`Output: ${OUT}`);

  const allFiles = [];
  for (const sub of INLINE_DIRS) {
    const dir = path.join(ASSETS, sub);
    const files = walk(dir);
    for (const f of files) {
      allFiles.push({ rel: `${sub}/${f.rel}`, abs: f.abs });
    }
    console.log(`  ${sub}: ${files.length} files`);
  }

  // Also inline the top-level assets/manifest.json — Pyodide host
  // reads it to know what engine files + wheels to mount.
  const manifestAbs = path.join(ASSETS, "manifest.json");
  if (fs.existsSync(manifestAbs)) {
    allFiles.push({ rel: "manifest.json", abs: manifestAbs });
    console.log(`  manifest.json: 1 file`);
  }

  // Sort for deterministic output (so the generated file is stable
  // across builds and diff-friendly in code review).
  allFiles.sort((a, b) => a.rel.localeCompare(b.rel));

  // Build the TS source. Use a JSON-string-escaped value for safety
  // — JSON.stringify handles every UTF-8 character, embedded quotes,
  // backslashes, and newlines correctly.
  const lines = [
    "// v0.2.91 — GENERATED BY scripts/inline-bundled-assets.mjs",
    "// DO NOT EDIT. Re-run `npm run inline-assets` to regenerate.",
    "//",
    "// Inlined plugin assets so BRAT installs work without an",
    "// `assets/` directory on disk. The runtime restores these to",
    "// the plugin directory on first onload (or whenever a file is",
    "// missing). See src/restore-inlined-assets.ts.",
    "",
    "export const BUNDLED_ASSETS: Record<string, string> = {",
  ];

  let totalBytes = 0;
  for (const f of allFiles) {
    const content = fs.readFileSync(f.abs, "utf8");
    totalBytes += Buffer.byteLength(content, "utf8");
    lines.push(`  ${JSON.stringify(f.rel)}: ${JSON.stringify(content)},`);
  }

  lines.push("};");
  lines.push("");

  fs.writeFileSync(OUT, lines.join("\n"));

  const kb = (totalBytes / 1024).toFixed(1);
  console.log(`Wrote ${allFiles.length} files, ${kb} KB total content.`);
  console.log(`Generated ${path.relative(ROOT, OUT)}`);
}

main();
